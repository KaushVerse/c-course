#include <stdio.h>

int main()
{

    // Integer
    int age = 22;

    // Character
    char grade = 'K';

    // Float
    float price = 10.5;

    // Double
    double pi = 3.1415926535;

    // Printing values
    printf("Age (int): %d\n", age);
    printf("Grade (char): %c\n", grade);
    printf("Price (float): %f\n", price);
    printf("PI (double): %lf\n", pi);

    return 0;
}
#include <stdio.h>

int main()
{

    // Short Integer
    short int a = 3200;

    // Long Integer
    long int b = 1234567890;

    // Signed Integer
    signed int c = -50;

    // Unsigned Integer
    unsigned int d = 50;

    // Printing Values
    printf("Short int: %hd\n", a);
    printf("Long int: %ld\n", b);
    printf("Signed int: %d\n", c);
    printf("Unsigned int: %u\n", d);
    return 0;
}